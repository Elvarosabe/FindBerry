public class Componentes
{

    private int imagen;
    private String text;

    public Componentes(int imagen, String text)
    {
        this.imagen = imagen;
        this.text = text;

    }


    public int getImagen() {
        return imagen;
    }

    public String getText() {
        return text;
    }
}
